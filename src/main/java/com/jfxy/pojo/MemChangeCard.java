package com.jfxy.pojo;

public class MemChangeCard {

	private Integer memid;
	private String newcard;
	private String copypwd;
	private String oldpwd;
	private String newpwd;
	private String newcardnumber;

	
	public String getNewcard() {
		return newcard;
	}

	public void setNewcard(String newcard) {
		this.newcard = newcard;
	}

	public String getCopypwd() {
		return copypwd;
	}

	public void setCopypwd(String copypwd) {
		this.copypwd = copypwd;
	}

	public String getOldpwd() {
		return oldpwd;
	}

	public void setOldpwd(String oldpwd) {
		this.oldpwd = oldpwd;
	}

	public String getNewpwd() {
		return newpwd;
	}

	public void setNewpwd(String newpwd) {
		this.newpwd = newpwd;
	}

	public String getNewcardnumber() {
		return newcardnumber;
	}

	public void setNewcardnumber(String newcardnumber) {
		this.newcardnumber = newcardnumber;
	}

	public Integer getMemid() {
		return memid;
	}

	public void setMemid(Integer memid) {
		this.memid = memid;
	}

	
	
	

}
